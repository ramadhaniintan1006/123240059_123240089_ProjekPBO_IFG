    public void tampilDataStasiun() {

    StasiunRepository repo =
            new StasiunRepository();

    ArrayList<Stasiun> list =
            repo.getAllStasiun();

    DefaultTableModel model =
            (DefaultTableModel)
            tableStasiun.getModel();

    model.setRowCount(0);

    for (Stasiun stasiun : list) {

        Object[] row = {

            stasiun.getIdStasiun(),
            stasiun.getNamaStasiun(),
            stasiun.getKota()
        };

        model.addRow(row);
    }
}

