    private void btnTambahJadwalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahJadwalActionPerformed
        // TODO add your handling code here:
        String nama =
        txtNamaStasiun.getText();

        String kota =
                txtKota.getText();

        Stasiun stasiun =
                new Stasiun(
                        0,
                        nama,
                        kota
                );

        StasiunRepository repo =
                new StasiunRepository();

        boolean sukses =
                repo.tambahStasiun(stasiun);

        if (sukses) {

            JOptionPane.showMessageDialog(
                    null,
                    "Stasiun berhasil ditambahkan!"
            );

          
            tampilDataStasiun();

            // KOSONGKAN FORM
            txtNamaStasiun.setText("");
            txtKota.setText("");

        } else {

            JOptionPane.showMessageDialog(
                    null,
                    "Gagal tambah stasiun!"
            );
        }
    }//GEN-LAST:event_btnTambahJadwalActionPerformed

    private void tableStasiunMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableStasiunMouseClicked
        // TODO add your handling code here:
        int row =
        tableStasiun.getSelectedRow();

        idStasiun =
                Integer.parseInt(
                        tableStasiun.getValueAt(row, 0)
                        .toString()
                );

        String nama =
                tableStasiun.getValueAt(row, 1)
                .toString();

        String kota =
                tableStasiun.getValueAt(row, 2)
                .toString();

        txtNamaStasiun.setText(nama);

        txtKota.setText(kota);
    }//GEN-LAST:event_tableStasiunMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String nama =
        txtNamaStasiun.getText();

        String kota =
                txtKota.getText();

        Stasiun stasiun =
                new Stasiun(
                        idStasiun,
                        nama,
                        kota
                );

        StasiunRepository repo =
                new StasiunRepository();

        boolean sukses =
                repo.updateStasiun(stasiun);

        if (sukses) {

            JOptionPane.showMessageDialog(
                    null,
                    "Stasiun berhasil diupdate!"
            );

            tampilDataStasiun();

            txtNamaStasiun.setText("");
            txtKota.setText("");

        } else {

            JOptionPane.showMessageDialog(
                    null,
                    "Gagal update stasiun!"
            );
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        StasiunRepository repo =
        new StasiunRepository();

        boolean sukses =
                repo.deleteStasiun(idStasiun);

        if (sukses) {

            JOptionPane.showMessageDialog(
                    null,
                    "Stasiun berhasil dihapus!"
            );

            tampilDataStasiun();

            txtNamaStasiun.setText("");
            txtKota.setText("");

        } else {

            JOptionPane.showMessageDialog(
                    null,
                    "Gagal hapus stasiun!"
            );
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

