    private void loadKursi() {

    KursiRepository repo =
            new KursiRepository();

    ArrayList<Kursi> list =
            repo.getKursiByJadwal(
                    idJadwal
            );

    for (Kursi k : list) {

        JButton btn =
                getButtonByNomor(
                        k.getNomorKursi()
                );

        if (btn == null) {
            continue;
        }

        if (k.getStatus().equals("Dipesan")) {

            btn.setBackground(Color.RED);

            btn.setOpaque(true);

            btn.setContentAreaFilled(true);

            btn.setEnabled(false);
        } else {

            btn.setBackground(Color.GREEN);

            btn.setOpaque(true);

            btn.setContentAreaFilled(true);

        }
    }
}
    
