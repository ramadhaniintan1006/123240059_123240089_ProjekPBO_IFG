    private void loadJadwal() {

    JadwalRepository repo =
            new JadwalRepository();

    ArrayList<Jadwal> list =
            repo.getAllJadwal();

        for (Jadwal j : list) {

            if (j.getIdJadwal() == idJadwal) {

                lblKereta.setText(
                        j.getNamaKereta()
                );

                lblRute.setText(
                        j.getAsal()
                        + " - "
                        + j.getTujuan()
                );

                break;
            }
        }
    }
    
