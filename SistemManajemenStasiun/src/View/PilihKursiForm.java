    private void btnPesanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesanActionPerformed
        // TODO add your handling code here:
        if (nomorKursiTerpilih.equals("")) {

        JOptionPane.showMessageDialog(
                null,
                "Pilih kursi terlebih dahulu!"
        );

            return;
        }

        KursiRepository kursiRepo =
                new KursiRepository();

        Kursi kursi =
                kursiRepo.getKursiByNomor(
                        idJadwal,
                        nomorKursiTerpilih
                );

        if (kursi == null) {

            JOptionPane.showMessageDialog(
                    null,
                    "Kursi tidak ditemukan!"
            );

            return;
        }

        String kodeBooking =
                "BK"
                + System.currentTimeMillis();

        Pemesanan pemesanan =
                new Pemesanan(
                        0,
                        userLogin.getIdUser(),
                        idJadwal,
                        kursi.getIdKursi(),
                        kodeBooking,
                        "Pending",
                        ""
                );

        PemesananRepository repo =
                new PemesananRepository();

        boolean sukses =
                repo.tambahPemesanan(
                        pemesanan
                );

        if (sukses) {

            kursiRepo.updateStatus(
                    kursi.getIdKursi(),
                    "Dipesan"
            );

            JOptionPane.showMessageDialog(
                    null,
                    "Booking berhasil!\nKode: "
                    + kodeBooking
            );
            
            Pembayaran pembayaran =
            new Pembayaran(
                kodeBooking,
                nomorKursiTerpilih,
                350000
            );

pembayaran.setVisible(true);

dispose();
            
            JButton btn =
                    getButtonByNomor(
                            nomorKursiTerpilih
                    );

            btn.setBackground(
                    Color.RED
            );

            btn.setEnabled(
                false
            );

        } else {

            JOptionPane.showMessageDialog(
                    null,
                    "Booking gagal!"
            );
        }
    }//GEN-LAST:event_btnPesanActionPerformed

