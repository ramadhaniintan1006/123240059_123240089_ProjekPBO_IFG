    private void btnTambahKeretaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahKeretaActionPerformed
        // TODO add your handling code here:
                String namaKereta =
                txtNamaKereta.getText();

        String kelas =
                cmbKelas.getSelectedItem().toString();

        int kapasitas =
                Integer.parseInt(
                        txtKapasitas.getText()
                );

        Kereta kereta =
                new Kereta(
                        0,
                        namaKereta,
                        kelas,
                        kapasitas
                );

        KeretaRepository repo =
                new KeretaRepository();

        boolean sukses =
                repo.tambahKereta(kereta);

        if (sukses) {

            JOptionPane.showMessageDialog(
                    null,
                    "Kereta berhasil ditambahkan!"
            );

            // REFRESH TABEL 😭✨
            tampilDataKereta();
            
            txtNamaKereta.setText("");
            txtKapasitas.setText("");
            cmbKelas.setSelectedIndex(0);

        } else {

            JOptionPane.showMessageDialog(
                    null,
                    "Gagal tambah kereta!"
            );
        }
    }//GEN-LAST:event_btnTambahKeretaActionPerformed

    private void tableKeretaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableKeretaMouseClicked
        // TODO add your handling code here:
        int selectedRow =
        tableKereta.getSelectedRow();

        selectedIdKereta =
                Integer.parseInt(
                        tableKereta
                        .getValueAt(selectedRow, 0)
                        .toString()
                );

        txtNamaKereta.setText(
                tableKereta
                .getValueAt(selectedRow, 1)
                .toString()
        );

        cmbKelas.setSelectedItem(
                tableKereta
                .getValueAt(selectedRow, 2)
                .toString()
        );

        txtKapasitas.setText(
                tableKereta
                .getValueAt(selectedRow, 3)
                .toString()
        );
    }//GEN-LAST:event_tableKeretaMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String namaKereta =
        txtNamaKereta.getText();

        String kelas =
                cmbKelas.getSelectedItem().toString();

        int kapasitas =
                Integer.parseInt(
                        txtKapasitas.getText()
                );

        Kereta kereta =
                new Kereta(
                        selectedIdKereta,
                        namaKereta,
                        kelas,
                        kapasitas
                );

        KeretaRepository repo =
                new KeretaRepository();

        boolean sukses =
                repo.updateKereta(kereta);

        if (sukses) {

            JOptionPane.showMessageDialog(
                    null,
                    "Data berhasil diupdate!"
            );

            tampilDataKereta();

            txtNamaKereta.setText("");
            txtKapasitas.setText("");
            cmbKelas.setSelectedIndex(0);

        } else {

            JOptionPane.showMessageDialog(
                    null,
                    "Gagal update data!"
            );
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

