    public void tampilDataKereta() {

        KeretaRepository repo =
                new KeretaRepository();

        ArrayList<Kereta> list =
                repo.getAllKereta();

        DefaultTableModel model =
                (DefaultTableModel)
                tableKereta.getModel();

        model.setRowCount(0);

        for (Kereta kereta : list) {

            Object[] row = {
                kereta.getIdKereta(),
                kereta.getNamaKereta(),
                kereta.getKelas(),
                kereta.getKapasitas()
            };

            model.addRow(row);
        }
    }
    
